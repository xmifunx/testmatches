import sortByConditions from '../index';

test('check sorting characters', () => {
  const team = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const sortedTeam = sortByConditions(team);
  expect(sortedTeam).toEqual([
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ]);
});
